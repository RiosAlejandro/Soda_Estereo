          <h1>Nada Personal</h1>
          <p>Nada Personal es el nombre del segundo álbum de estudio de la banda de rock argentina Soda Stereo, y fue editado en Argentina el 21 de noviembre de 1985. Fue reeditado en CD por primera vez en 1991.</p>
          <p>El disco fue grabado y mezclado en los estudios Moebio de la ciudad de Buenos Aires (Argentina) con el técnico Mariano López como ingeniero de sonido. Bien recibido por la crítica especializada, significó para la banda un aumento enorme de su popularidad. Solo en Argentina vendió alrededor de 180000 copias.</p>
          <img src="img/1985-Nada_Personal-Trasera.jpg" alt="1985" style="width: 50%; margin-top: 10px; margin-bottom: 15px; display: block; margin-left: auto; margin-right: auto;">
          <p>Este disco mostró una evolución de la banda hacia un estilo cada vez más propio, se trató de letras más profundas e, incluso, canciones que no sólo eran para bailar, como sucedía con el trabajo anterior. Un ejemplo de ello son los temas "Cuando pase el temblor" y "Ecos", el último de la placa, que mostró una faceta distinta a lo que se veía viendo hasta ese momento.</p>
          <p>Destacan en este disco, entre otras, las canciones "Nada personal", "Juego de seducción" y la ya mencionada "Cuando pase el temblor".</p>
          <p>Si bien el disco fue editado en 1985, su explosión llegó un año más tarde. El 11 de abril de 1986 la banda presentó oficialmente su segundo álbum en el "Templo del rock". Fue el primero de los cuatro conciertos que dieron ante 6 mil personas por función.</p>
          <p>Con Nada Personal, el grupo empezaba a volar alto y, en poco tiempo, -a partir de Signos, su tercer disco- llegaría a ocupar un lugar privilegiado: ser la banda de rock más importante de la región.</p>
