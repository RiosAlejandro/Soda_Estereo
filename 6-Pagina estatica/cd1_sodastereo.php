					<h1>Soda Stereo</h1>
					<p>Soda Stereo es el álbum debut del mitico grupo argentino Soda Stereo, grabado y editado en 1984, La grabación se hizo en los estudios de CBS, en la calle Paraguay al 1500.</p>
					<p>La producción de este álbum debut estuvo a cargo de Federico Moura, cantante y lider de la banda argentina Virus.</p>
					<p>Fue presentado el 1 de octubre del mismo año en el subsuelo del local de Pumper Nic en el centro de Buenos Aires. </p>
					<img src="img/1984-Soda_Stereo-Trasera.jpg" alt="1984" style="width:50%; margin-top: 10px; margin-bottom: 15px; display: block; margin-left: auto; margin-right: auto; ">
					<p> El álbum presenta un estilo muy influenciado por la música post-punk, ska y new wave. Los   sonidos de bandas como The Police, The Cure e incluso, Men at work, se pueden apreciar en su música e imagen. Destacan en este disco los primeros grandes éxitos de Soda Stereo, tales como "Tele-Ka", "¿Por qué no puedo ser del Jet-Set?", "Te hacen falta vitaminas", "Trátame suavemente", "Un misil en mi placard" y "Sobredosis de TV".</p>
					<p> Por este álbum, Soda Stereo obtuvo disco de platino en Argentina, y doble disco de platino en Perú y Chile.</p>
					<p> Contiene una fuerte crítica al hedonismo de la sociedad argentina de la época, lo que se refleja claramente en muchos de sus temas, los cuales siguen siendo recordados hasta el dia de hoy.</p>
