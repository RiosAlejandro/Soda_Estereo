					<h1>Doble Vida</h1>
                     <p>Doble Vida es el cuarto álbum de estudio del grupo de rock argentino Soda Stereo, grabado y editado en 1988. Producido por Carlos Alomar, este fue el segundo de los únicos 2 álbumes de Soda Stereo producidos por alguien fuera de la banda, siendo el otro Soda Stereo, el álbum debut, que fuera producido por Federico Moura de Virus.</p>
                     <p>Carlos Alomar preparó un estructurado y novedoso proceso de creación del álbum a la manera de los trabajos estadounidenses, distinto a lo que se había visto hasta ese entonces en la música de Latinoamérica. Convocó a principios de junio a la banda para grabarlo en Nueva York, siendo este uno de los primeros álbumes de rock de Latinoamérica de la historia en ser grabados en Estados Unidos. Finalmente luego de poco más de un mes, Doble Vida fue presentado el 7 de julio en el club "The Tunnel" de Nueva York, y lanzado el 23 de septiembre.</p>
                     <img src="img/1988-Doble_Vida-Trasera.jpg" alt="1988" style="width: 50%; margin-top: 10px; margin-bottom: 15px; display: block; margin-left: auto; margin-right: auto;">
                     <p>Doble Vida marcó el fin de la primera etapa de Soda Stereo, que consistía en una imagen exótica, y abrió camino a la madurez de la banda. Además, fue en el transcurso de la gira de promoción que decidieron renunciar a Ohanian Producciones y crear su empresa propia, Triple Producciones, con la que editarían trabajos en el futuro.</p> 
                     <p>Doble Vida en particular está cargado de una fuerte influencia de la música afroamericana, con una marcada presencia en particular del funk, y también toques de soul, R&B, y hasta un rap en el tema "En el borde".</p>
                     <p>Este trabajo discográfico le permitió al grupo conquistar el mercado estadounidense y convirtió a Soda Stereo en la primera banda de América Latina en conseguirlo. La gira los llevó incluso al histórico "Hollywood Palace" de Los Ángeles (hoy llamado "Avalon Hollywood"), todo un hito en la historia del rock.</p> 
                     <p>El grupo volaba alto y nunca más iba a dejar de hacerlo.</p>
