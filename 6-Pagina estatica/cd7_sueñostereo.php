					<h1>Sueño Stereo</h1>
          <p>Sueño Stereo es el séptimo y último álbum de estudio de la banda de rock argentina Soda Stereo, lanzado al mercado en 1995. Fue grabado en Supersónico, Buenos Aires por Eduardo Bergallo y mezclado en Matrix, Londres por Clive Goddard y Bergallo.</p> 
          <p>Las canciones de Sueño Stereo, el séptimo trabajo de estudio de la banda, dan la sensación de flotar. Como la distancia entre un sueño y una pesadilla, Soda Stereo nunca había sonado tan bien y nunca había estado tan cerca de la desintegración.</p>
          <p>En lo musical, Sueño Stereo resultó un trabajo mucho más pop respecto que Dynamo, pero sin abusar de la clave estrofa-estribillo-estrofa. De hecho, según Cerati, el disco “casi adolece de estribillos”.
          Sueño Stereo es un material diverso en texturas musicales, lo cual venía a reafirmar -aunque de manera casi innecesaria- que Soda era una banda multifacética y exploradora.</p>
          <img src="img/1995-Sueno_Stereo-Trasera.jpg" alt="1995" style="width: 50%; margin-top: 10px; margin-bottom: 15px; display: block; margin-left: auto; margin-right: auto;">
          <p>El disco salió el 21 de junio de 1995 y fue presentado con un raid de nueve shows en el teatro Gran Rex durante septiembre.</p>
          <p>En sólo quince días de ventas en Hispanoamérica el álbum fue doble disco de platino. El álbum fue el eje de la extensa Gira Sueño Stereo que realizaron por Venezuela, Colombia, Perú, Chile, Honduras, Panamá, Costa Rica, México y Estados Unidos, que comenzó el 8 de septiembre de 1995 en Buenos Aires, extendiéndose hasta el 24 de abril de 1996 en Santiago de Chile.</p>
          <p>Debido al éxito de éste disco y al ser además el último álbum de estudio de la banda, para gran parte del público y la crítica, Soda Stereo se disolvió en el tope de su creatividad (musicalmente hablando), ya que el álbum sirvió para ratificar el estatus de la banda como una de las más influyentes en la Historia del Rock Latino, pero además se convirtió en uno de los más relevantes de su mítica carrera.</p>
