					<h2>Héctor Pedro Juan Bosio</h2>
					<p>Nacio en San Fernando, Buenos Aires, el 1 de octubre de 1958. Más conocido como Zeta Bosio, es un músico de rock, productor discográfico y DJ argentino. Fue el bajista de la mítica e influyente banda de rock en español Soda Stereo; debido a esto, es considerado uno de los músicos más importantes de rock latino y en español.</p>
					<p>Inició su contacto con la música a los 11 años, edad en la que escuchó por primera vez The Beatles, decidiéndose a aprender a tocar el bajo encima de los discos.
					Mientras estaba en el colegio formó dos bandas, Agua y La Banda de San Francisco. Luego se internó en la marina, y con su primer salario se compró un bajo en Puerto Rico. En la armada integró la orquesta y perfeccionó sus conocimientos musicales.</p>
					<p>Cuando volvió a Buenos Aires se inscribió en la carrera de publicidad de la Universidad de El Salvador y formó parte del grupo The Morgan.</p>
					<p>Gustavo y Zeta compartían los mismos gustos y sueños musicales y comenzaron una búsqueda para integrar un grupo punk rock inspirado en The Police (que fue a la Argentina ese año), con temas propios en español. Ambos decidieron visitar a Charly Alberti (hijo del percusionista Tito Alberti y cuatro años más joven que Bosio), para escucharlo tocar en la batería de su padre. Allí se formó Soda Stereo.</p>
