					<h2>Gustavo Adrián Cerati</h2>
					<p>Nació el 11 de agosto de 1959 en el barrio de Barracas, ciudad de Buenos Aires, Argentina, hijo mayor del ingeniero y contador Juan José Cerati y Lilian Clark.</p>
					<p>fue un músico, cantautor, compositor y productor discográfico argentino. Obtuvo reconocimiento internacional por haber sido el cantante, guitarrista y compositor principal de la banda de rock Soda Stereo. Es considerado uno de los músicos más importantes, populares e influyentes del rock latinoamericano.</p>
					<p>Su carrera musical comienza en el año 1983, aunque antes de eso, ya a los nueve años estudiaba guitarra, a los doce conformó un trío con el cual se presentaba en fiestas particulares, participaba de todos los eventos que se realizaban en el colegio religioso al que concurría, y llegó a dirigir el coro de la iglesia.</p>
					<p>Sus primeras influencias musicales fueron grupos como King Crimson y especialmente The Beatles. También David Bowie, Pink Floyd y guitarristas como Jimmy Page (Led Zeppelin) y Ritchie Blackmore (Deep Purple).</p>
                    <p>En el año 1979 en la Universidad de El Salvador cursando la carrera de Publicidad, Gustavo Cerati conoce a Héctor "Zeta" Bosio. Mientras estudiaba tocó en dos bandas: una de rock and roll y blues, y otra de fusión.</p> 
                    <p>A partir del año 1982 los dos músicos comienzan a proyectar la formación de una banda en la que tocarían temas propios. En ese momento conocen a Charly Alberti, y de ahí en más se constituye formalmente Soda Stereo.</p>
