					<p>En 2005, 2006 y 2007, Zeta conduce un programa de televisión llamado Rock Road en el canal argentino Much Music donde comenta su recorrido por diversos festivales europeos.</p>
					<p>En junio de 2007, Soda Stereo anunció su vuelta a los escenarios con una gira. Entre octubre y diciembre de ese año, Zeta Bosio y Soda Stereo tocan por toda Latinomérica rompiendo una vez más todos los records, en la gira que denominaron Me verás volver.</p>
					<p>Durante 2013, Zeta fue contactado por Beto Cuevas, vocalista de la agrupación chilena de pop/rock La Ley, banda que recientemente se había vuelto a reunir tras varios años separados, para saber si le interesaría ingresar al grupo como bajista.</p>
					<p>Finalmente todo se concreta y el 29 de enero de 2014. Zeta Bosio debuta con La Ley en el Festival Movistar Free Music en Mar del Plata en Argentina, y están confirmados para participar en el Festival de Viña del Mar 2014, escenario que Zeta no pisaba desde 1987 junto a Soda Stereo.</p>
					<p>En mayo de 2015 Zeta anunció una nueva vuelta a los escenarios, pero esta vez con banda a estrenar: Shoot The Radio. El nuevo grupo que fusiona Rock y Música Electrónica lo conforma junto a Fernando Montemurro (LSDA) y distintos artistas invitados.</p>
					<p>En 2016 lanzó "Yo conozco ese lugar", su autobiografía en la que cuenta memorias desde su infancia, pasando por la era de Soda Stereo, hasta sucesos más recientes. Tenía planeado lanzarlo para 2014, pero la publicación del libro sufrió un retraso de dos años.</p>
