					<p>Tras la disolución de Soda Stereo, desarrolló una exitosa carrera solista, que consta de cinco álbumes de estudio y diversas incursiones junto a otros músicos.</p>
					<p>El 15 de mayo de 2010 fue internado por una descompensación,en el Centro Médico Docente La Trinidad al terminar su presentación en el campo de fútbol de la Universidad Simón Bolívar, Caracas, Venezuela. El 18 de mayo se supo que realmente se trataba de un ACV (accidente cerebrovascular). Más tarde fue intervenido quirúrgicamente ante la gravedad del cuadro.</p>
					<p>El lunes 7 de junio de 2010 es trasladado a Argentina en compañía de su madre, Lilian Clark y su hermana, Laura Cerati.</p>
					<p>En la noche del domingo 24 de octubre de 2010 Cerati ingresó a la Clínica ALCLA. Durante su estadía en esta clínica, persistió el mismo estado neurológico que el informado antes de realizar el traslado, indicó su médico.</p>
					<p>Falleció el 4 de septiembre de 2014 tras pasar cuatro años en coma. El deceso se produjo en torno a las 9:00 hora local, sin sufrimiento. La noticia fue confirmada en la cuenta oficial de Cerati en la red social Twitter,así como mediante la lectura de un comunicado por parte de sus familiares y los médicos de la clínica donde se encontraba internado.</p>
					<p>El día siguiente al de su fallecimiento, tras un velatorio realizado en la Legislatura de la Ciudad de Buenos Aires, sus restos fueron trasladados al Cementerio de la Chacarita.</p>
