				<div class="col-md-6">
					<p>En un impasse que duró casi 2 años, Charly decide experimentar en el trabajo alejado de sus compañeros de grupo, realizando junto a Deborah de Corral (su pareja para ese momento) y su hermano Andrés Alberti, su primer disco como compositor y productor. El proyecto se llamó PLUM y fue editado solamente en Argentina, agotando su edición limitada.</p>
					<p>Entre 1997 y 2005, Alberti se dedica casi exclusivamente al desarrollo de Cybrel Digital Entertainment, con el objetivo de implementar y generar contenidos y aplicaciones relacionados con la música, basados sobre nuevas tecnologías y orientados principalmente al mercado de habla hispana.</p>
					<p>luego de mucha búsqueda y experimentación musical, en 2005 funda lo que sería su nueva banda: Mole, el grupo lo conforman junto a él, Andrés Alberti (hermano de Charly, 2.ª guitarra y ex Santos Inocentes), Sergio Bufi (voz y 1.ª guitarra) y Ezequiel Dasso (bajo, coros). La primera placa del grupo, titulada simplemente MOLE se edita a principios de 2007 únicamente en Argentina, aunque se tiene previsto su lanzamiento a nivel latinoamericano en 2008. El 26 de marzo de 2008, Mole resultó ganador en la categoría Mejor Álbum Nuevo Artista de Rock en los Premios Carlos Gardel.</p>
					<p>En 2007 tuvo lugar la gira de regreso de Soda Stereo, Me Verás Volver 2007, Después de la gira, cada uno de los integrantes volvió a sus proyectos personales y carreras en solitario.</p>
					<p>Revolución 21 (R-21) fue fundada por Charly Alberti tras comprender la profundidad de la crisis climática y los desafíos y oportunidades que esta presenta para Latinoamérica. El reconocido músico decidió lanzar su propia fundación para difundir la crisis ambiental global, aprovechando su capacidad para alcanzar a millones de personas.</p>
