					<h2>Carlos Alberto Ficicchia Gigliotti</h2>
					<p>Nacio en Buenos Aires, el 27 de marzo de 1963. Más conocido como Charly Alberti, es un músico de rock argentino, hijo del baterista de jazz y musica caribeña Tito Alberti. Fue el baterista de la mítica e influyente banda de rock Soda Stereo; debido a esto es considerado uno de los músicos más importantes del rock latino y en español.</p>
					<p>Desde muy pequeño sintió gran fascinación por la naturaleza y los animales, especialmente los peces, se dedicó cuidadosamente al criado de diversas especies y esta pasión le permitió disfrutar de su personalidad pensante y solitaria.</p>
					<p>A los seis años comenzó a estudiar batería de manera particular con profesores de la talla de Alberto Alcalá y el "Oso" Piccardi. Sin embargo, es conocido que en un principio se inició en el estudio de la batería más por deseo de su padre, Tito Alberti, que por voluntad propia; ya para su adolescencia había cambiado de percepción con respecto a la batería, convirtiéndose en su gran pasión.</p>
					<p>En 1981 conoce a quienes se convertirían en sus compañeros de grupo por 15 años: Gustavo Cerati y Zeta Bosio. Este contacto se dio a través de la hermana de Gustavo, Laura Cerati.</p>
					<p>Su aporte a Soda Stereo, más allá de la batería, tiene que ver con la experimentación e inclusión de elementos electrónicos, tales como samplers y sintetizadores.</p>
				</div>
