					<h1>Signos</h1>
          <p>Signos es el nombre del tercer álbum de estudio del grupo argentino, Soda Stereo, grabado y editado en 1986. Fue producido por Soda Stereo y es considerado por la mayoría de los críticos como el segundo mejor álbum de la banda, después de Canción animal.</p>
          <p>El tercer trabajo de la banda perfectamente pudo grabarse en el exterior, el presupuesto lo permitía. Sin embargo, tomó forma tras largas jornadas de improvisaciones a oscuras en los estudios Moebio. Signos es el producto de estados anímicos atípicos, experiencias que rozaban la crisis y del genio de Cerati, que hiló un batallón de letras conceptuales en una sola noche.</p>
          <p>Ese mismo año emprendieron una segunda gira por el continente con una repercusión aún mayor. El grupo se presentó en Perú, Paraguay, Bolivia, Ecuador, Colombia, Venezuela, Costa Rica y México . Realizaron 22 conciertos en 17 ciudades frente a casi 350.000 personas, abriendo así la idea misma de un "rock latino", más allá de la nacionalidad de cada banda.</p>
          <img src="img/1986-Signos-Trasera.jpg" alt="1986" style="width: 50%; margin-top: 10px; margin-bottom: 15px; display: block; margin-left: auto; margin-right: auto;">
          <p>Las invitaciones no se hicieron esperar y fue así como, en febrero de 1987, cuatro meses después de haber lanzado el disco, Soda Stereo lo presentaba en el Festival Internacional de Viña del Mar, desatando la “Sodamanía” y catapultando su carrera al extranjero de forma definitiva.</p>
          <p>Soda Stereo era la primera banda en la historia del Rock en español que conseguía tal éxito a nivel continental.</p> 
          <p>Signos fue la bisagra definitiva en la carrera de Soda Stereo:El 86 fue el año del despegue y la confirmación masiva. Nada volvió a ser lo mismo luego de esa gira larga, exitosa y demoledora.</p>
